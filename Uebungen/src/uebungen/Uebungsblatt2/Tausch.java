public class Tausch {

    public static void main(String[] args){

        int x = 5;
        int y = 7;
        int t = x;


        System.out.println(x);
        System.out.println(y);

        //Tausch
        x = y;
        y = t;

        System.out.println("\n" + x);
        System.out.println(y);
    }





}
